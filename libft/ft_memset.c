#include "libft.h"

void	*ft_memset(void *src, int bat, int a)
{
	int		i;
	char	*ptr;

	i = 0;
	ptr = src;
	while (i < a)
	{
		ptr[i] = bat;
		i++;
	}
	return (src);
}
