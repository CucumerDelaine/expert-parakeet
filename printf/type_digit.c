#include "ft_printf.h"

int	ft_isdigit(int c)
{
	return (c >= 48 && c <= 57);
}

int	ft_type_list(char c)
{
	if (c == 'c' || c == 's' || c == 'p' || c == 'd' || c == 'i'
		|| c == 'u' || c == 'x' || c == 'X' || c == '%')
		return (1);
	return (0);
}

int	ft_flag_list(char c)
{
	if (c == '-' || c == '0' || c == '*' || c == '.' || c == ' ')
		return (1);
	return (0);
}
